#Atribuir sem repetir a variavel

variavel = 5
puts "valor inicial: #{variavel}"

variavel = variavel + 1
puts "soma 1: #{variavel}"

variavel += 1
puts "soma 1: #{variavel}"

variavel -= 1
puts "subtrai 1: #{variavel}"

variavel *= 2
puts "multiplica por 2: #{variavel}"

variavel /= 2
puts "divide por 2: #{variavel}"

variavel %= 4
puts "modulo 4: #{variavel}"

variavel **= 3
puts "elevado a 3: #{variavel}"